/*
El objeto Math proporciona propiedades y métodos para constantes matemáticas y funciones matemáticas.
*/

let num1 = 9.54578;
let num2 = 2.6541;
let num3 = -100;
let num4 = 100;

console.log(Math.abs(num3)); //100
console.log(Math.ceil(num1)); //10
console.log(Math.floor(num1)); //9
console.log(Math.round(num1)); //10
console.log(Math.max(num1,num2,num3,num4)); //100
console.log(Math.min(num1,num2,num3,num4)); //-100
console.log(Math.pow(num4,2)); //10000
console.log(Math.sqrt(num4)); //10


console.log(Math.random()); //0.0000000
console.log(Math.E); //2.718281828459045
console.log(Math.PI); //3.141592653589793