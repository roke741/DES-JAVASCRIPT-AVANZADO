// crear clase Persona con constructor
class Persona{
    nombre; 
    edad;
    constructor(nombre, edad){
        this.nombre = nombre;
        this.edad = edad;
    }
}
// crear objeto persona
var persona = new Persona("Juan", 25);
// mostrar objeto persona
console.log(persona);


